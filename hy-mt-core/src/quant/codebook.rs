/// 32-entry codebook, indexed by `(sign << 4) | code`.
///
/// Each entry packs four ternary lanes into 8 bits using a 2-bits-per-lane
/// encoding `0b00 → -1`, `0b01 → 0`, `0b10 → +1` (i.e. `q - 1` ∈ {-1, 0, +1}).
/// In every group of four weights exactly one lane is the "zero lane",
/// realising the 3:4 sparsity pattern the format relies on.
pub const STQ1_0_CODEBOOK: [u8; 32] = [
    // sign = 0 (first non-zero lane is +1)
    0xA9, 0x89, 0x29, 0x09, 0xA6, 0x86, 0x26, 0x06, //
    0x9A, 0x92, 0x1A, 0x12, 0x6A, 0x62, 0x4A, 0x42, //
    // sign = 1 (every non-zero lane negated)
    0x01, 0x21, 0x81, 0xA1, 0x04, 0x24, 0x84, 0xA4, //
    0x10, 0x18, 0x90, 0x98, 0x40, 0x48, 0x60, 0x68, //
];

