/// Pre-decoded `f32` ternary lanes for every codebook entry — 32 entries
/// of 4 lanes in {-1.0, 0.0, +1.0}. Indexed by `(sign << 4) | code`, this
/// turns the bit-twiddling inner loop in [`super::matmul::stq_dot_row_f32`]
/// into a 4-wide branchless FMA against `row[base..][..4]`. Built from
/// [`STQ1_0_CODEBOOK`] at compile time so the two stay in lock-step.
pub const STQ1_0_LUT_F32: [[f32; 4]; 32] = build_lut_f32();

const fn build_lut_f32() -> [[f32; 4]; 32] {
    let mut out = [[0.0f32; 4]; 32];
    let mut i = 0;
    while i < 32 {
        let qpack = STQ1_0_CODEBOOK[i];
        let mut p = 0;
        while p < 4 {
            // Each lane is a 2-bit code in {0, 1, 2}, mapping to {-1, 0, +1}
            // after subtracting 1.
            let q = ((qpack >> (2 * p)) & 0x3) as i32 - 1;
            out[i][p] = q as f32;
            p += 1;
        }
        i += 1;
    }
    out
}

